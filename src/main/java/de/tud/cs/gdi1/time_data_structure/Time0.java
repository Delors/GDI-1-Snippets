package de.tud.cs.gdi1.time_data_structure;

public class Time0 {

    final int hours;
    final int mins;
    final int secs;

    public Time0(int h, int m, int s) {
	this.hours = h;
	this.mins = m;
	this.secs = s;
    }

    public int getHours() {
	return this.hours;
    }

    public int getMins() {
	return this.mins;
    }

    public int getSecs() {
	return this.secs;
    }

}
